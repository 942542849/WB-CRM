package model;


public class Customer {
	private int id;
	private String name;
	private String tel;
	private String area;
	private int user_id;
	private int level_id;
	private int pleased_id;
	private int credit_id;
	private String address;
	
	
	private String username;
	private String levelname;
	private String pleasedname;
	private String creditname;
	
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getLevelname() {
		return levelname;
	}
	public void setLevelname(String levelname) {
		this.levelname = levelname;
	}
	public String getPleasedname() {
		return pleasedname;
	}
	public void setPleasedname(String pleasedname) {
		this.pleasedname = pleasedname;
	}
	public String getCreditname() {
		return creditname;
	}
	public void setCreditname(String creditname) {
		this.creditname = creditname;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getLevel_id() {
		return level_id;
	}
	public void setLevel_id(int level_id) {
		this.level_id = level_id;
	}
	public int getPleased_id() {
		return pleased_id;
	}
	public void setPleased_id(int pleased_id) {
		this.pleased_id = pleased_id;
	}
	public int getCredit_id() {
		return credit_id;
	}
	public void setCredit_id(int credit_id) {
		this.credit_id = credit_id;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}


	
}
